numbers = [4, 6, 2, 9, 3]

print(6 in numbers) # True (check if element contains in list)
print(2 in numbers) # True
print(5 in numbers) # False
print(5 not in numbers) # True (check if element does not contain in list)

# operations
print(len(numbers)) 
print(min(numbers))
print(max(numbers))
print(sum(numbers))

